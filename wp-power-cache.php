		public function add_cache_id_to_admin_bar( $wp_admin_bar ) {
			if ( ! current_user_can( 'manage_options' ) || is_admin() ) {
				return;
			}

			$settings = (array) get_option( 'wp_power_cache_settings' );
			
			if ( isset($settings['debug_flag']) && $settings['debug_flag'] == 1 ) {
				$exists = $this->cache_exists();
				$status = $exists ? 'Cached' : 'Dynamic';
				$color  = $exists ? '#46b450' : '#ffb900';
				$total_size = $this->get_cache_size();
				
				// Main Status Node
				$wp_admin_bar->add_node( array(
					'id'    => 'wrpc-cache-status',
					'title' => '<span style="color:' . $color . ';">● WP Cache: ' . $status . '</span>',
					'href'  => admin_url( 'admin.php?page=setting-power-cache' ),
				) );

				// Sub-node: Show Total Cache Size
				$wp_admin_bar->add_node( array(
					'id'     => 'wrpc-cache-size',
					'parent' => 'wrpc-cache-status',
					'title'  => 'Total Size: ' . $total_size,
				) );

				// Sub-node: Clear THIS specific page
				if ( $exists ) {
					$wp_admin_bar->add_node( array(
						'id'     => 'wrpc-clear-this',
						'parent' => 'wrpc-cache-status',
						'title'  => 'Clear This Page Cache',
						'href'   => wp_nonce_url( add_query_arg( 'wrpc_clear_single', '1' ), 'wrpc_clear_single_action' ),
					) );
				}

				// NEW Sub-node: Empty ALL Cache
				$wp_admin_bar->add_node( array(
					'id'     => 'wrpc-empty-all',
					'parent' => 'wrpc-cache-status',
					'title'  => '<span style="color:#d63638;">Empty All Cache</span>',
					'href'   => wp_nonce_url( add_query_arg( 'wrpc_empty_all', '1' ), 'wrpc_empty_all_action' ),
					'meta'   => array(
						'onclick' => 'return confirm("Delete ALL cached files?");'
					),
				) );
			}
		}

		public function handle_single_clear_request() {
			// Check for the "Clear Single" trigger and security nonce
			if ( isset( $_GET['wrpc_clear_single'] ) && check_admin_referer( 'wrpc_clear_single_action' ) ) {
				
				// Ensure we have the current cache file name set
				if ( empty( $this->currentCacheFile ) ) {
					$this->currentCacheFile = $this->get_query_parameters( null );
				}

				$path = $this->cache_file_path();

				// Delete the file if it exists
				if ( file_exists( $path ) ) {
					unlink( $path );
				}

				// Redirect back to the clean URL
				wp_safe_redirect( add_query_arg( 'cache_cleared', '1', remove_query_arg( array( 'wrpc_clear_single', '_wpnonce' ) ) ) );
				exit;
			}
		}

		private function get_cache_size() {
			$size = 0;
			$directory = WRPC_ROOT_DIR . $this->cacheFolder;
			
			if ( ! is_dir( $directory ) ) return '0 B';

			foreach ( new RecursiveIteratorIterator( new RecursiveDirectoryIterator( $directory ) ) as $file ) {
				if ( $file->isFile() ) {
					$size += $file->getSize();
				}
			}

			if ( $size === 0 ) return '0 B';

			$units = array( 'B', 'KB', 'MB', 'GB' );
			$power = $size > 0 ? floor( log( $size, 1024 ) ) : 0;
			return number_format( $size / pow( 1024, $power ), 2 ) . ' ' . $units[$power];
		}

		public function unified_clear_cache_handler() {

			// 1. GLOBAL CLEAR (All files)
			if ( (isset($_GET['wrpc_empty_all']) && check_admin_referer('wrpc_empty_all_action')) || isset($_POST['clear_all_cache']) ) {
				if ( isset($_POST['clear_all_cache']) ) {
					check_admin_referer( 'wrpc_clear_cache_action', 'wrpc_nonce' );
				}

				wrpc_recursive_dir_delete( WRPC_ROOT_DIR . $this->cacheFolder );

				if ( ! file_exists( WRPC_ROOT_DIR . $this->cacheFolder ) ) {
					wp_mkdir_p( WRPC_ROOT_DIR . $this->cacheFolder );
				}

				// Redirect with the 'all' flag
				wp_safe_redirect( add_query_arg( 'wrpc_cleared', 'all', remove_query_arg( array( 'wrpc_empty_all', '_wpnonce' ) ) ) );
				exit;
			}

			// 2. SINGLE PAGE CLEAR (One file)
			if ( isset( $_GET['wrpc_clear_single'] ) && check_admin_referer( 'wrpc_clear_single_action' ) ) {
				
				$filename = $this->get_standard_filename() . '.html';
				$path = WRPC_ROOT_DIR . $this->cacheFolder . '/' . $filename;

				if ( file_exists( $path ) ) {
					unlink( $path );
					// Redirect with the 'single' flag
					wp_safe_redirect( add_query_arg( 'wrpc_cleared', 'single', remove_query_arg( array( 'wrpc_clear_single', '_wpnonce' ) ) ) );
					exit;
				}
				
				// If file didn't exist, still redirect but without the success message
				wp_safe_redirect( remove_query_arg( array( 'wrpc_clear_single', '_wpnonce' ) ) );
				exit;
			}
		}

		private function get_standard_filename() {
			if ( is_front_page() || is_home() ) {
				return 'index';
			}

			// Standardize the URI by removing query strings and trailing slashes
			$uri = strtok($_SERVER['REQUEST_URI'], '?');
			$uri = rtrim($uri, '/');

			// If it's still empty after trimming, it's the homepage
			if ( empty($uri) ) {
				return 'index';
			}

			// Return the MD5 of the cleaned URI
			return md5( $uri );
		}

