			if ( $type === 'all' ) {
				$message = '✓ Entire Website Cache Cleared';
				$bg_color = '#d63638'; // Red for global clear
			} elseif ( $type === 'single' ) {
				$message = '✓ Current Page Cache Cleared';
			}

			if ( $message ) {
				echo '<div id="wrpc-notice" style="position: fixed; top: 50px; right: 20px; z-index: 99999; background: ' . $bg_color . '; color: #fff; padding: 12px 24px; border-radius: 5px; box-shadow: 0 4px 15px rgba(0,0,0,0.3); font-family: -apple-system, BlinkMacSystemFont, sans-serif; font-weight: 500; animation: wrpc_fadeout 4s forwards;">';
				echo $message;
				echo '</div>';
				echo '<style>@keyframes wrpc_fadeout { 0% {opacity: 0; transform: translateY(-20px);} 10% {opacity: 1; transform: translateY(0);} 80% {opacity: 1;} 100% {opacity: 0; visibility: hidden;} }</style>';
			}
		}

